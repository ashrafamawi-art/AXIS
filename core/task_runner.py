"""
AXIS TaskRunner — interprets natural-language task strings and dispatches
them to registered handler coroutines.

Pattern matching is intentionally simple: each handler declares a list of
trigger keywords and a regex to extract arguments.  Add new handlers by
appending to _HANDLERS in this module.
"""

import re
import time
from datetime import datetime, timezone
from pathlib import Path
from typing import Callable, Awaitable

from utils.logger import get_logger

log = get_logger("axis.task_runner")


class TaskResult:
    def __init__(self, success: bool, message: str, artifacts: dict = None):
        self.success   = success
        self.message   = message
        self.artifacts = artifacts or {}
        self.timestamp = datetime.now(timezone.utc).isoformat()

    def __str__(self):
        status = "OK" if self.success else "FAIL"
        return f"[{status}] {self.message}"


class TaskRunner:
    def __init__(self, graph, logger=None):
        self._graph = graph
        self._log   = logger or log

    async def run(self, description: str) -> TaskResult:
        desc = description.strip()
        self._log.info(f"Running task: {desc!r}")

        for handler_fn, keywords in _HANDLERS:
            if any(kw in desc.lower() for kw in keywords):
                try:
                    result = await handler_fn(desc, self._graph, self._log)
                    self._log.info(str(result))
                    return result
                except Exception as exc:
                    msg = f"Handler failed: {exc}"
                    self._log.error(msg)
                    return TaskResult(False, msg)

        return await _handle_fallback(desc, self._graph, self._log)


